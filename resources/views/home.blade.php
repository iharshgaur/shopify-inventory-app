  <script>
    $(document).ready(function () {
      $('.changeRecentActivityFilter').click(function(e) {
        e.preventDefault();
        var el = $(this);
        var timeDiff = el.attr('data-timeDiff');
        console.log(timeDiff);
        $.ajax({
          type: 'GET', 
          url: "{{route('recent.activity')}}?timeDiff="+timeDiff,
          async: false,
          success: function(response) {
            $('.activity').html(response.html);
            $('#timeDiffSelect').html('Last '+timeDiff+' days');
          }
        })
      });
    })
  </script>
